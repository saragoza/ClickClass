           <h1>1  ¿Qué tengo que hacer para formar parte de la comunidad ClickClass?</h1>
           <p>Formar parte de la red de usuarios de ClickClass es muy sencillo, simplemente tienes que acceder al formulario de registro haciendo click en el link Registrarse de la welcome page.</p>
           <p>Introduce tu nombre de usuario, email y password y con un click serás un miembro más de la comunidad.</p>
           <h1>2  ¿Cómo me logueo en la aplicación?</h1>
           <p>Haciendo click en en link Conectarse de la welcome page accedes a un formulario en el que debes introducir tu email de usuario y password.</p>
           <p>Si así lo deseas, puedes marcar la opción Recordarme, y tu password aparecerá automáticamente en el campo del formulario la próxima vez que te quieras conectar.</p>
           <h1>3  ¿Y qué pasa si olvido mi password?</h1>
	       <p>No te preocupes, tiene solución. En el formulario para Conectarse tienes el link Has Olvidado Tu Password?</p>
           <p>Siguiendo ese link llegarás a un formulario que te pedirá tu dirección de correo de usuario, y te enviaremos un correo con un enlace a otro formulario en que podrás cambiar tu contraseña.</p>
           <h1>4  ¿Cómo puedo compartir archivos?</h1>
           <p>Seleccionando la opción Compartir accedes a un formulario en el que se te pedirán una serie de datos:</p>
           <ol>
               <li><strong>Descripción:</strong> Consiste en una breve definición del archivo. Procura que sea lo más descriptiva y concreta posible para que el resto de usuarios sepan qué van a encontrarse.</li>
               <li><strong>Tags de búsqueda:</strong> De forma opcional, puedes introducir tags que faciliten la búsqueda del archivo. Estos tags no saldrán en la información detallada ni en los listados.</li>
               <li><strong>Tipo de archivo:</strong> Selecciona el tipo de archivo que mejor encaje con el recurso que vas a compartir.</li>
               <li><strong>Ruta del archivo:</strong> Selecciona la ruta donde se encuentra el archivo a compartir.</li>
               <li><strong>Información adicional:</strong> Si con la descripción no tienes suficiente, puedes hacer uso de este campo, que te permite describir con más detalle el contenido el contenido de tu archivo.</li>
           </ol>
           <p>Haz click en Enviar, y si tu archivo se ha compartido correctamente visualizarás una ventana verde con un mensaje confirmando el éxito de la operación. En cambio, si ha habido algún problema aparecerá una ventana roja avisando de la incidencia.</p>
           <h1>5  ¿Puedo acceder a los archivos que he compartido?</h1>
           <p>Puedes acceder a ellos y a los archivos de otros usuarios sobre los que te hayan otorgado permisos desde la opción de Mis Archivos.</p>
	       <p>Aparecerá un listado con información resumida de todos tus archivos en propiedad o con permisos, con distintas opciones sobre cada uno de ellos:</p>
	       <ol>
	           <li><strong>Detalle:</strong> acceso a información detallada del archivo. Desde esa pantalla puedes descargar tu archivo.</li>
	           <li><strong>Descargar:</strong> puedes descargar tu archivo directamente seleccionando esta opción.</li>
	           <li><strong>Editar:</strong> para modificar la información relativa al archivo o incluso sustituirlo por otro.</li>
	           <li><strong>Permisos:</strong> sólo para tus archivos en propiedad. Otorga permisos de edición sobre tus archivos a otros usuarios.</li>
	           <li><strong>Eliminar:</strong> sólo para tus archivos en propiedad. Elimina el archivo seleccionado si consideras que ya no debe seguir formando parte del catálogo ClickClass.</li>
	       </ol>
	       <h1>6  ¿Cómo funcionan los permisos a otros usuarios?</h1>
	       <p>La gestión de permisos sobre tus archivos es extremadamente sencilla. Al acceder a Permisos, aparece un listado de los usuarios con un filtrador que te permitirá buscar el/los usuarios que te interesen.</p>
	       <p>Al lado de cada uno de ellos aparecerá el tipo de permisos que tienen y un botón para otorgar o eliminar permisos.</p>
           <p>Con la opción Volver regresas al listado de tus archivos.</p>
           <h1>7  ¿Y puedo acceder a los archivos de los demás?</h1>
           <p>Por supuesto que sí, es lo que le da sentido a todo esto. Seleccionando Buscar tienes a tu alcance todos los archivos de nuestro catálogo. Podrás acotar tu búsqueda con la ayuda del filtrador en la esquina superior derecha de la tabla.</p>
           <p>Para cada archivo del listado dispones de dos posibles acciones:</p>
           <ol>
               <li><strong>Detalle:</strong> acceso a información detallada del archivo. Desde esa pantalla puedes descargar el archivo.</li>
               <li><strong>Descargar:</strong> puedes descargar el archivo directamente seleccionando esta opción.</li>
           </ol>
           <h1> 8  ¿Tengo el control de la información de mi perfil?</h1>
           <p>Tú eres el administrador de tu información personal. Haciendo click en tu nombre de usuario, en la parte derecha de la cabecera, puedes, además de salir de la aplicación, acceder a tu perfil.</p>
           <p>En esa pantalla podrás ver toda tu información personal, y dispondrás de dos opciones:</p>
           <ol>
               <li><strong>Editar perfil:</strong> accederás a un formulario con tus datos, y podrás modificar los que quieras, salvo el id de usuario. El password también podrás modificarlo desde allí si así lo deseas. Si quieres conservar el mismo simplemente deja ese campo en blanco.</li>
               <li><strong>Eliminar perfil:</strong> puedes darte de baja en el momento en que lo desees. Simplemente ten en cuenta que aquellos archivos que no elimines antes de marcharte permanecerán en el catálogo.</li>
           </ol>
